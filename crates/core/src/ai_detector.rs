use crate::models::AiSource;
use once_cell::sync::Lazy;
use regex::Regex;

static CURSOR_RE: Lazy<Regex> = Lazy::new(|| Regex::new(r"(?i)cursor").unwrap());
static COPILOT_RE: Lazy<Regex> = Lazy::new(|| Regex::new(r"(?i)copilot").unwrap());
static CLAUDE_RE: Lazy<Regex> = Lazy::new(|| Regex::new(r"(?i)claude|anthropic").unwrap());
static WINDSURF_RE: Lazy<Regex> = Lazy::new(|| Regex::new(r"(?i)windsurf|codeium").unwrap());
static AIDER_RE: Lazy<Regex> = Lazy::new(|| Regex::new(r"(?i)aider").unwrap());
static AI_GENERIC_RE: Lazy<Regex> =
    Lazy::new(|| Regex::new(r"(?i)ai[- ]?generated|ai[- ]?authored|generated by ai").unwrap());

pub struct AiDetector;

impl AiDetector {
    pub fn new() -> Self {
        Self
    }

    pub fn detect(&self, commit_message: &str, author: &str) -> Option<AiSource> {
        if CURSOR_RE.is_match(commit_message) {
            return Some(AiSource::Cursor);
        }
        if COPILOT_RE.is_match(commit_message) {
            return Some(AiSource::Copilot);
        }
        if CLAUDE_RE.is_match(commit_message) {
            return Some(AiSource::ClaudeCode);
        }
        if WINDSURF_RE.is_match(commit_message) {
            return Some(AiSource::Windsurf);
        }
        if AIDER_RE.is_match(commit_message) {
            return Some(AiSource::Aider);
        }
        if AI_GENERIC_RE.is_match(commit_message) {
            return Some(AiSource::Unknown);
        }
        if Self::is_bot_author(author) {
            Some(AiSource::Unknown)
        } else {
            None
        }
    }

    pub fn is_ai_commit(&self, commit_message: &str, author: &str) -> bool {
        self.detect(commit_message, author).is_some()
    }

    fn is_bot_author(author: &str) -> bool {
        let lower = author.to_lowercase();
        lower.contains("bot")
            || lower.contains("[bot]")
            || lower.contains("dependabot")
            || lower.contains("renovate")
            || lower.contains("github-actions")
    }

    pub fn extract_intent(&self, commit_message: &str) -> String {
        let lines: Vec<&str> = commit_message.lines().collect();
        if let Some(first) = lines.first() {
            first.trim().to_string()
        } else {
            commit_message.trim().to_string()
        }
    }

    pub fn detect_file_ai_content(&self, content: &str) -> bool {
        let indicators = [
            "TODO: This code was generated by",
            "// Generated by AI",
            "/* AI-generated",
            "# This file was auto-generated",
            "/// @ai-generated",
        ];

        indicators.iter().any(|ind| content.contains(ind))
    }
}
